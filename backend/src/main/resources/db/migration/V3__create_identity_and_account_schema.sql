CREATE TABLE membership_tiers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    code VARCHAR(20) NOT NULL,
    display_name VARCHAR(80) NOT NULL,
    default_discount_percentage NUMERIC(5, 2) NOT NULL,
    sort_order INTEGER NOT NULL,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT membership_tiers_pkey PRIMARY KEY (id),
    CONSTRAINT membership_tiers_code_key UNIQUE (code),
    CONSTRAINT membership_tiers_code_valid CHECK (code IN ('REGULAR', 'BRONZE', 'SILVER', 'GOLD')),
    CONSTRAINT membership_tiers_name_not_blank CHECK (char_length(btrim(display_name)) > 0),
    CONSTRAINT membership_tiers_discount_valid CHECK (default_discount_percentage BETWEEN 0 AND 100),
    CONSTRAINT membership_tiers_sort_nonnegative CHECK (sort_order >= 0)
);

