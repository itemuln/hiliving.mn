CREATE TABLE email_verification_tokens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    token_hash VARCHAR(64) NOT NULL,
    expires_at TIMESTAMPTZ NOT NULL,
    used_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    requested_ip VARCHAR(45),
    CONSTRAINT email_verification_tokens_user_fkey FOREIGN KEY (user_id)
        REFERENCES users (id) ON DELETE CASCADE,
    CONSTRAINT email_verification_tokens_hash_key UNIQUE (token_hash),
    CONSTRAINT email_verification_tokens_hash_format CHECK (token_hash ~ '^[0-9a-f]{64}$'),
    CONSTRAINT email_verification_tokens_expiry_valid CHECK (expires_at > created_at),
    CONSTRAINT email_verification_tokens_used_valid CHECK (used_at IS NULL OR used_at >= created_at)
);

CREATE INDEX email_verification_tokens_user_active_idx
    ON email_verification_tokens (user_id, created_at DESC)
    WHERE used_at IS NULL;
CREATE INDEX email_verification_tokens_expiry_idx
    ON email_verification_tokens (expires_at)
    WHERE used_at IS NULL;

